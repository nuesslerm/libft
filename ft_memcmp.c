#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char const	*p1;
	unsigned char const	*p2;
	size_t				i;

	p1 = (unsigned char const *)s1;
	p2 = (unsigned char const *)s2;
	i = 0;
	while (i < n)
	{
		if (p1[i] != p2[i])
			return (p1[i] - p2[i]);
		i++;
	}
	return (0);
}
